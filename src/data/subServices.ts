/**
 * Get a specific sub-service by parent slug and sub-service slug
 * @param parentSlug - The parent service slug (e.g., "web-development")
 * @param subSlug - The sub-service slug (e.g., "landing-pages")
 * @returns The sub-service detail or undefined if not found
 */
export const getSubServiceBySlug = (
  parentSlug: string,
  subSlug: string
): SubServiceDetail | undefined => {
  return allSubServices.find(
    (subService) =>
      subService.parentSlug === parentSlug && subService.slug === subSlug
  );
};

/**
 * Get all sub-services for a specific parent service
 * @param parentSlug - The parent service slug (e.g., "web-development")
 * @returns Array of sub-service details for the parent
 */
export const getSubServicesByParent = (
  parentSlug: string
): SubServiceDetail[] => {
  return allSubServices.filter(
    (subService) => subService.parentSlug === parentSlug
  );
};

